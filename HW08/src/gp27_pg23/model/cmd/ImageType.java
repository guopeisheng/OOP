package gp27_pg23.model.cmd;

import java.io.Serializable;

import javax.swing.ImageIcon;

/**
 * Image data type
 * @author Gao Pan
 *
 */
public class ImageType implements Serializable {

	/**
	 * UID
	 */
	private static final long serialVersionUID = 4318283890847934840L;

	/**
	 * The Image Icon
	 */
	private ImageIcon ii;

	/**
	 * Construct with a ImageIcon
	 * @param ii The ImageIcon
	 */
	public ImageType(ImageIcon ii) {
		this.ii = ii;
	}

	/**
	 * Get the ImageIcon
	 * @return The ImageIcon
	 */
	public ImageIcon getImage() {
		return ii;
	}
}
